<template>
<div class="container-fluid">
 <div>
    <transition-group name="fade" tag="div">
      <div v-for="(i,index) in caption" :key="index" class="motto d-flex flex-column align-items-center justify-content-center rtext">
            <h3>{{i[currentIndex].motto}}</h3>
            <p>{{i[currentIndex].sub_motto}}</p>
      </div>
    </transition-group>
    <a class="prev" @click="prev" href="#">Pre</a>
    <a class="next" @click="next" href="#"> Next</a>

    
  </div>
</div>
 
</template>

<script>
export default {
  name: "Slider2",
  props: {
      caption:Array
  },
  data() {
    return {
      timer: null,
      currentIndex: 0,
      
    };
  },

  mounted: function() {
    this.startSlide();
  },

  methods: {
    startSlide: function() {
      this.timer = setInterval(this.next, 4000);
    },

    next: function() {
      this.currentIndex += 1;
    },
    prev: function() {
      this.currentIndex -= 1;
    }
  },

  computed: {
    currentImg: function() {
      return this.caption[Math.abs(this.currentIndex) % this.caption.length];
    }
  }
};
</script>

